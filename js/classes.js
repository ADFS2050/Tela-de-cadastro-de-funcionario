class User {
    constructor(name, email, password, phone, dob) {
        this.name = name;
        this.email = email;
        this.password = password;
        this.phone = phone;
        this.dob = dob;
    }
}

class Item {
    constructor(name, description, category, condition) {
        this.name = name;
        this.description = description;
        this.category = category;
        this.condition = condition;
    }
}
